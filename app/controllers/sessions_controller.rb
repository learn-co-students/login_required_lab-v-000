class SessionsController < ApplicationController

  def new
  end

  def show
  end

  def create
    session[:name] = params[:name]
    if current_user == nil || current_user == ''
      redirect_to '/login'
    else
      render 'secrets/show'
    end
  end

  def destroy
    session.delete :name
    redirect_to '/login'
  end
end
